public class ArrayIntro {  
    public static void main(String [] args){
        int [] marks=new int[6]; //array is created of size 6
        System.out.println(marks[3]); //print the value which is at 4 postion  or at the 3 inde of the array
        int age[]={12,23,45,21 };
        System.out.println(age[2]);
        age[2]=67; // use to change the value of the array 
        System.out.println(age[2]);
        // to print the array 
        for(i=0;i<age.length;i++){
            System.out.println(age[i]);
        }
    
    }
    
}
